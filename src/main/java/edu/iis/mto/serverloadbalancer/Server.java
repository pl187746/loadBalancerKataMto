package edu.iis.mto.serverloadbalancer;

import java.util.ArrayList;
import java.util.List;

public class Server {

	public static final double MAXIMUM_LOAD = 100.0;
	private int capacity;
	private double currentLoadPercentage;
	private List<Vm> vmList = new ArrayList<Vm>();

	public Server(int capacity) {
		this.capacity = capacity;
	}

	public double getCurrentLoadPercentage() {
		return currentLoadPercentage;
	}
	
	public void addVm(Vm vm) {
		vmList.add(vm);
		currentLoadPercentage += MAXIMUM_LOAD * vm.getSize() / capacity;
	}

	public boolean contains(Vm vm) {
		return vmList.contains(vm);
	}

	public int getCapacity() {
		return capacity;
	}

	public int getVmCount() {
		return vmList.size();
	}

	public boolean canFit(Vm vm) {
		return currentLoadPercentage + (MAXIMUM_LOAD * vm.getSize() / capacity) <= MAXIMUM_LOAD;
	}

}
